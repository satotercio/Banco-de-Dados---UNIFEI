    private void btnLutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLutarActionPerformed
        int HP3Tot = jogador.getHP3();
        int HP4Tot = adversario.getHP4();
        
        while(jogador.getHP3() > 0 && adversario.getHP4() > 0 ){
            
            if(adversario.getHP4() - jogador.getAtaque3() < 0){
                adversario.setHP4(0);
            }else{
            adversario.setHP4(adversario.getHP4() - jogador.getAtaque3());
            }
            JOptionPane.showMessageDialog(this,
                    "(" + adversario.getNomeInimigo() + ") HP: " + adversario.getHP4()
                    + "/" + HP4Tot);
            if(adversario.getHP4() - jogador.getAtaque3() < 0){
                break;
            }
            if(jogador.getHP3() - adversario.getAtaque4() < 0){
                jogador.setHP3(0);
            }else{
            jogador.setHP3(jogador.getHP3() - adversario.getAtaque4());
            }
            
            JOptionPane.showMessageDialog(this,
                    "(" + jogador.getNome() + ") HP: " + jogador.getHP3()
                    + "/" + HP3Tot);
            
        }
        if(jogador.getHP3() > adversario.getHP4()){
            JOptionPane.showMessageDialog(this,jogador.getNome()+ " venceu!");
            int x = NumerosRandomicos.randomInt(0, 3);
            switch(x){
                    case 0: JOptionPane.showMessageDialog(this,jogador.getNome()+ " ganhou +3 de HP!");
                            jogador.setHP3(jogador.getHP3()+3);
                            pdao.update(jogador);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 1: JOptionPane.showMessageDialog(this,jogador.getNome()+ " ganhou +2 de MP!");
                            jogador.setMP3(jogador.getMP3()+2);
                            pdao.update(jogador);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 2: JOptionPane.showMessageDialog(this,jogador.getNome()+ " ganhou +2 de DEF!");
                            jogador.setDefesa3(jogador.getDefesa3()+2);
                            pdao.update(jogador);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 3: JOptionPane.showMessageDialog(this,jogador.getNome()+ " ganhou +1 de ATK!");
                            jogador.setAtaque3(jogador.getAtaque3()+1);
                            pdao.update(jogador);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
            }    
        }
        else{ 
            JOptionPane.showMessageDialog(this,adversario.getNomeInimigo()+ " venceu!");
                        int x = NumerosRandomicos.randomInt(0, 3);
            switch(x){
                    case 0: JOptionPane.showMessageDialog(this,adversario.getNomeInimigo()+ " ganhou +5 de HP!");
                            adversario.setHP4(adversario.getHP4()+5);
                            idao.update(adversario);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 1: JOptionPane.showMessageDialog(this,adversario.getNomeInimigo()+ " ganhou +6 de MP!");
                            adversario.setMP4(adversario.getMP4()+6);
                            idao.update(adversario);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 2: JOptionPane.showMessageDialog(this,adversario.getNomeInimigo()+ " ganhou +7 de DEF!");
                            adversario.setDefesa4(adversario.getDefesa4()+7);
                            idao.update(adversario);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                    
                    case 3: JOptionPane.showMessageDialog(this,adversario.getNomeInimigo()+ " ganhou +9 de ATK!");
                            adversario.setAtaque4(adversario.getAtaque4()+9);
                            idao.update(adversario);
                            JOptionPane.showMessageDialog(this,"Atributo atualizado!");
                            break;
                
            }
        }
        btnLutar.setEnabled(false);
        btn1.setEnabled(true);
        btn2.setEnabled(true);
        cbx1.setEnabled(true);
        cbx2.setEnabled(true);
    }//GEN-LAST:event_btnLutarActionPerformed

